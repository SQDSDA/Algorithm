package com.msb.test01;

public class 变量学习 {
    public static void main(String[] args) {
        // 变量的声明
        // int 变量
        int age;
        // 变量不可以重复定义
        // 这行代码会报错 int age;
        // 可以同时定义多个变量
        int a, b;
        // 变量的定义和赋值可以写在同一行(和py不一样)
        int c = 30, d = 40;
        // 变量的赋值
        // 变量的使用
        System.out.println(c);
        System.out.println(d);
    }
}
